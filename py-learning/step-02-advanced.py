# ============================================
# 1. 列表（List）- 像 JavaScript 的陣列
# ============================================

# 列表用 [] 包起來，用逗號分隔
my_list = [100, 500, 1000]  # 這是列表
print("列表內容：", my_list)

# 取得列表中的元素（從 0 開始）
print("第一個元素：", my_list[0])  # 100
print("第二個元素：", my_list[1])  # 500
print("第三個元素：", my_list[2])  # 1000

# 列表可以包含不同類型的資料
mixed_list = ["小明", 25, 1000.5, True]
print("混合列表：", mixed_list)

# 列表可以新增元素
my_list.append(2000)  # 在最後面新增
print("新增後：", my_list)

# 列表長度
print("列表長度：", len(my_list))

# ============================================
# 2. 字典（Dictionary）- 像 JavaScript 的物件
# ============================================

# 字典用 {} 包起來，用 key: value 的格式
my_dict = {
    "name": "Ryan",
    "age": 25,
    "wallet": 1000
}
print("字典內容：", my_dict)

# 取得字典的值（用 key）
print("名字：", my_dict["name"])
print("年齡：", my_dict["age"])
print("錢包：", my_dict["wallet"])

# 新增或修改字典的值
my_dict["wallet"] = 2000  # 修改
my_dict["room"] = "CGIGOJP1"  # 新增
print("修改後：", my_dict)

# ============================================
# 3. 條件判斷（if/else）
# ============================================

# 基本語法
age = 25

if age >= 18:
    print("已成年")
else:
    print("未成年")

# 多條件判斷
wallet = 1000
bet_amount = 200

if wallet >= bet_amount:
    print("可以下注")
else:
    print("錢包餘額不足")

# elif（else if）
score = 85

if score >= 90:
    print("優秀")
elif score >= 80:
    print("良好")
elif score >= 60:
    print("及格")
else:
    print("不及格")

# ============================================
# 4. 迴圈（for）- 遍歷列表
# ============================================

# 遍歷列表
bets = [100, 500, 1000]

print("所有下注金額：")
for bet in bets:
    print(f"  - {bet} 元")

# 遍歷列表並取得索引
bets = [100, 500, 1000]
for index, bet in enumerate(bets):
    print(f"第 {index + 1} 個下注：{bet} 元")

# 計算總和
bets = [100, 500, 1000]
total = 0
for bet in bets:
    total = total + bet
print(f"總金額：{total}")

# ============================================
# 5. 迴圈（while）- 條件迴圈
# ============================================

# 基本 while 迴圈
count = 0
while count < 3:
    print(f"計數：{count}")
    count = count + 1

# 實際例子：下注直到錢包不足
wallet = 1000
bet_amount = 200
round_count = 0

while wallet >= bet_amount:
    wallet = wallet - bet_amount
    round_count = round_count + 1
    print(f"第 {round_count} 輪，剩餘：{wallet} 元")

print(f"總共可以下注 {round_count} 輪")

# ============================================
# 6. 列表和字典的組合
# ============================================

# 列表裡面放字典（常見的資料結構）
test_results = [
    {"test_name": "下注 100", "result": "通過", "amount": 100},
    {"test_name": "下注 200", "result": "通過", "amount": 200},
    {"test_name": "下注 300", "result": "通過", "amount": 300},
    {"test_name": "下注 500", "result": "失敗", "amount": 500}
]

print("測試結果：")
for test in test_results:
    print(f"  {test['test_name']} - {test['result']} - 金額：{test['amount']}")

