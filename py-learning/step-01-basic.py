# Python 不需要宣告關鍵字，直接賦值即可
# 注意：Python 沒有 const，但習慣上大寫變數名表示常數
my_name = "小明"  # 這是可以改變的變數

# Python 習慣用底線命名（snake_case），不是駝峰式（camelCase）
my_age = 25

# 注意：Python 不需要分號 ;
# 注意：文字可以用 " " 或 ' ' 包起來，兩者都可以
