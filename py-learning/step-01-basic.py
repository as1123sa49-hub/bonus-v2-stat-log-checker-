# ============================================
# 2. 數字和文字
# ============================================

number1 = 100        # 這是數字（整數 int）
text1 = "100"        # 這是文字（字串 str）

# 數字可以做計算
result1 = number1 + 50  # 結果是 150

# 文字不能直接和數字相加（會報錯）
# result2 = text1 + 50  # 這會出錯！

# 文字可以連接文字
result2 = text1 + "50"  # 結果是 "10050"（文字）

# 如果要轉換型別
result3 = int(text1) + 50  # 把文字轉成數字：150
result4 = str(number1) + "50"  # 把數字轉成文字："10050"

# ============================================
# 3. 印出東西（print）- 在終端機顯示
# ============================================

print("這是我第一次用 Python！")
print("我的名字是：", my_name)
print("我的年齡是：", my_age)

# Python 的 print 可以用 f-string（格式化字串，推薦方式）
print(f"我的名字是：{my_name}")
print(f"我的年齡是：{my_age}")

