# 整數除法（只要整數部分）
divide_int = b // a       # 結果：2（整數）

# 取餘數
remainder = b % a         # 結果：0

print("a + b =", sum_result)
print("a * b =", multiply)
print("b / a =", divide)
print("b // a =", divide_int)  # 整數除法
print("b % a =", remainder)   # 餘數

